package com.zhita.model.manage;

public class Result {
	
	private String accountNo;
	
	private String name;
	
	private String idCardCore;
	
	private String bankPreMobile;
	
	private String result;
	
	private String message;
	
	private Integer messagetype;

	public String getAccountNo() {
		return accountNo;
	}

	public void setAccountNo(String accountNo) {
		this.accountNo = accountNo;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getIdCardCore() {
		return idCardCore;
	}

	public void setIdCardCore(String idCardCore) {
		this.idCardCore = idCardCore;
	}

	public String getBankPreMobile() {
		return bankPreMobile;
	}

	public void setBankPreMobile(String bankPreMobile) {
		this.bankPreMobile = bankPreMobile;
	}

	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public Integer getMessagetype() {
		return messagetype;
	}

	public void setMessagetype(Integer messagetype) {
		this.messagetype = messagetype;
	}
	
	

}
